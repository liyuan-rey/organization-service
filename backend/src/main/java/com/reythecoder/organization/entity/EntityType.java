package com.reythecoder.organization.entity;

/**
 * Entity type enumeration for organization tree nodes.
 */
public enum EntityType {
    /**
     * Virtual root node.
     */
    ROOT,

    /**
     * Group entity type.
     */
    GROUP,

    /**
     * Department entity type.
     */
    DEPARTMENT,

    /**
     * Personnel entity type.
     */
    PERSONNEL
}
